package net.rezxis.mchosting.database.object.server;

import lombok.Getter;
import lombok.Setter;

@Getter@Setter
public class DBShopItembase {
	private String name;
	private String itemType;
	private String cmd;
	private long price;
	private long earned;
	
	public DBShopItembase(String name, String itemType, long price, String cmd, long earned) {
		this.name = name;
		this.itemType = itemType;
		this.price = price;
		this.cmd = cmd;
		this.earned = earned;
	}
}